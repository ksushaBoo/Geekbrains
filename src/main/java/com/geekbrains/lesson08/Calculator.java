package com.geekbrains.lesson08;

public class Calculator {

    public Calculator() {

    }

    public int sumOperation(float a, float b) {
        return (int) (a + b);
    }

    public int minusOperation(float a, float b) {
        return (int) (a - b);
    }

    public int multiplyOperation(float a, float b) {
        return (int) (a * b);
    }

    public float devisionOperation(float a, float b) {

        return a / b;
    }
}
